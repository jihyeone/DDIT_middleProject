package kr.or.ddit.middle.vo;

public class AttendanceVO {
	
	private String att_no;
	private String stu_id;
	private String att_date;
	private String att_entr;
	private String att_leaf;
	private String attyn;
	
	public String getAtt_no() {
		return att_no;
	}
	public void setAtt_no(String att_no) {
		this.att_no = att_no;
	}
	public String getStu_id() {
		return stu_id;
	}
	public void setStu_id(String stu_id) {
		this.stu_id = stu_id;
	}
	public String getAtt_date() {
		return att_date;
	}
	public void setAtt_date(String att_date) {
		this.att_date = att_date;
	}
	public String getAtt_entr() {
		return att_entr;
	}
	public void setAtt_entr(String att_entr) {
		this.att_entr = att_entr;
	}
	public String getAtt_leaf() {
		return att_leaf;
	}
	public void setAtt_leaf(String att_leaf) {
		this.att_leaf = att_leaf;
	}
	public String getAttyn() {
		return attyn;
	}
	public void setAttyn(String attyn) {
		this.attyn = attyn;
	}
	
}
