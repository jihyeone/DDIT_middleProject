package kr.or.ddit.middle.vo;

public class LectureVO {
	
	private String lect_id;
	private String lect_nm;
	private String lect_time;
	private String rm_no;
	private String pro_id;
	private String class_no;
	private String lect_totaltime;
	
	public String getLect_id() {
		return lect_id;
	}
	public void setLect_id(String lect_id) {
		this.lect_id = lect_id;
	}
	public String getLect_nm() {
		return lect_nm;
	}
	public void setLect_nm(String lect_nm) {
		this.lect_nm = lect_nm;
	}
	public String getLect_time() {
		return lect_time;
	}
	public void setLect_time(String lect_time) {
		this.lect_time = lect_time;
	}
	public String getRm_no() {
		return rm_no;
	}
	public void setRm_no(String rm_no) {
		this.rm_no = rm_no;
	}
	public String getPro_id() {
		return pro_id;
	}
	public void setPro_id(String pro_id) {
		this.pro_id = pro_id;
	}
	public String getClass_no() {
		return class_no;
	}
	public void setClass_no(String class_no) {
		this.class_no = class_no;
	}
	public String getLect_totaltime() {
		return lect_totaltime;
	}
	public void setLect_totaltime(String lect_totaltime) {
		this.lect_totaltime = lect_totaltime;
	}
	
}
